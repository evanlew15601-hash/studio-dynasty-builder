function toPublicDomainIP(source: PublicDomainSource): PublicDomainIP {
  return {
    id: source.id,
    name: source.title,
    domainType: source.domainType ?? 'literature',
    dateEnteredDomain: source.dateEnteredDomain ?? '1900-01-01',
    coreElements: source.themes,
    genreFlexibility: (source.genre as any) || ['drama'],
    notableAdaptations: [],
    reputationScore: 70,
    adaptationFatigue: 0,
    lastAdaptationDate: undefined,
    culturalRelevance: 70,
    requiredElements: [],
    suggestedCharacters: source.suggestedCharacters,
    description: source.description,
    cost: 0,
  };
}

export const BASE_PUBLIC_DOMAIN_CATALOG: ReadonlyArray<PublicDomainIP> = PUBLIC_DOMAIN_SOURCES.map(toPublicDomainIP);

export function getPublicDomainCatalog(count?: number): PublicDomainIP[] {
  if (typeof count !== 'number') return [...BASE_PUBLIC_DOMAIN_CATALOG];
  const desired = Math.max(0, Math.floor(count));
  return BASE_PUBLIC_DOMAIN_CATALOG.slice(0, Math.min(desired, BASE_PUBLIC_DOMAIN_CATALOG.length));
}
